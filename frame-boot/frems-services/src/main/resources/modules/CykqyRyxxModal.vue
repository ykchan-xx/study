        this.visible = true;
        this.$nextTick(() => {
          this.form.setFieldsValue(pick(this.model,'tx','sj','qx','zwxm','ywxm','sex','gjdq','csrq','zjhm','zjzl','ssqybm','zw','lxdh','sjrjsj','sjrjka','zsdjsj','zsdz','dwmc','qsrq','jsrq','jhcjsj','jhcjka','jhrjsj','jhrjka','gzqk','bz','lrr','lrsj','zhxgr','zhxgsj'))
